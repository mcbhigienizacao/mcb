let p_text = document.querySelector('.p-text');
function selectBi() {
    p_text.forEach((search)=>
        search.classList.add('p-text')
)
    this.classList.remove('p-text');
}
let biExp = document.querySelector('.bi-plus-circle');
biExp.addEventListener('click', function(){
    p_text.classList.toggle('p-text')
});

